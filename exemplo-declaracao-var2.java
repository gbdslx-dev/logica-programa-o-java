package br.com.generation.aula01;

public class Exemplo02 {

	public static void main(String[] args) {
		
		int var1;
		double var2;
		
		var1 = 10;
		var2 = 10.0;
		
		System.out.println("Valor da 1º variavel: " + var1);
		System.out.println("Valor da 2º variavel: " + var2);
		
		System.out.println();
		
		var1 = var1 / 3;
		var2 = var2 / 3;
		
		System.out.println("Valor da 1º variavel após a divisão: " + var1);
		System.out.println("Valor da 2º variavel após a divisão: " + var2);

	}

}
